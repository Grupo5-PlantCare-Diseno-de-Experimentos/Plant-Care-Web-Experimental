ALTER TABLE public.premium_leads ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can view their premium leads" ON public.premium_leads
    FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can insert their premium leads" ON public.premium_leads
    FOR INSERT WITH CHECK (auth.uid() = user_id);

CREATE INDEX IF NOT EXISTS idx_premium_leads_user_id ON public.premium_leads(user_id);

-- 7. Feature Interaction Logs Table (Tracking Plan 8.2.8)
-- Tabla de auditoría para eventos de interacción (select_promotion, view_promotion,
-- purchase_simulation, click_alert_link). Se escribe por lotes (batching) desde el frontend.
CREATE TABLE IF NOT EXISTS public.feature_interaction_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
    event_name TEXT NOT NULL,      -- select_promotion | view_promotion | purchase_simulation | click_alert_link
    promotion_id TEXT,
    promotion_name TEXT,
    location TEXT,
    metadata JSONB,
    occurred_at TIMESTAMP WITH TIME ZONE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.feature_interaction_logs ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can view their interaction logs" ON public.feature_interaction_logs
    FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can insert their interaction logs" ON public.feature_interaction_logs
    FOR INSERT WITH CHECK (auth.uid() = user_id);

CREATE INDEX IF NOT EXISTS idx_feature_logs_user_id ON public.feature_interaction_logs(user_id);
CREATE INDEX IF NOT EXISTS idx_feature_logs_event ON public.feature_interaction_logs(event_name);


-- 8. User Achievements Table (Gamificación · Hipótesis 5 — Insignia "Cuidador Experto")
